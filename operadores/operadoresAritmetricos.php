<?php 
// OPERADOR IDENTIDAD +
// OPERADOR NEGACION -

// SE RESPETAN LA REGLA DE LOS SIGNOS
$number = -10;
echo -$number;
echo "\n";
// OPERADOR ADICION

$number1 = 10;
$number2 = 8;
echo $number1 + $number2;
echo "\n";

// OPERADOR SUSTRACCION

echo $number1 - $number2;
echo "\n";

// OPERADOR MULTIPLICACION

echo $number1 * $number2;
echo "\n";

// OPERADOR DIVISION
echo $number1 / $number2;
echo "\n";

// OPERADOR MODULO
// (RESIDUO DE UNA DIVISION)
echo $number1 % $number2;
echo "\n";

// OPERADOR EXPONENCIACION (Potencia)

echo $number1 ** 2;

?>