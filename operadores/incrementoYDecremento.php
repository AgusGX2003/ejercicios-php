<?php 

// operador pre-incremento
$numero = 20;

echo ++$numero;
echo "\n";
// operador pos-incremento
// SE IMPRIME EL NUMERO Y LUEGO SE AGREGA LA ASIGNACION 
$numero2 = 20;
echo $numero2++;
echo "\n";
echo $numero2;
echo "\n";
// operador pre-decremento
$numero = 20;
echo --$numero;
echo "\n";
// operador pos-decremento
$numero = 20;
echo $numero--;
echo "\n";
echo $numero;
?>